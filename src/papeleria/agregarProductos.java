    private void agregarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarProductosActionPerformed
        String nombre = nombretext.getText();
        String descripcion = descriptext.getText();
        String categoria = categtext.getText();
        String precio = preciotext.getText();

// Verificar si todos los campos estÃ¡n llenos
        if (nombre.isEmpty() || descripcion.isEmpty() || categoria.isEmpty() || precio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.");
            return;
        }

// Convertir el precio a BigDecimal para manejarlo adecuadamente
        BigDecimal precioDecimal = null;
        try {
            precioDecimal = new BigDecimal(precio);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un nÃºmero vÃ¡lido.");
            return;
        }

// Conectar a la base de datos
        try {
            String url = "jdbc:postgresql://localhost:5432/Papeleria";
            String user = "postgres";
            String password = "9656";

            Connection con = DriverManager.getConnection(url, user, password);

            // Cambiar el esquema de bÃºsqueda
            Statement stmtSchema = con.createStatement();
            stmtSchema.execute("SET search_path TO ciber_action");
            stmtSchema.close();

            // Consulta SQL para insertar un nuevo producto
            String sql = "INSERT INTO producto (nombre, descripcion, categoria, precio) VALUES (?, ?, ?, ?)";
            PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setString(1, nombre);
            stmt.setString(2, descripcion);
            stmt.setString(3, categoria);
            stmt.setBigDecimal(4, precioDecimal);  // Usar BigDecimal para el precio

            int rowsInserted = stmt.executeUpdate();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Producto agregado exitosamente.");
            }

            stmt.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al agregar el producto: " + e.getMessage());
        }

    }//GEN-LAST:event_agregarProductosActionPerformed

