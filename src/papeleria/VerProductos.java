    private void borrarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarProdActionPerformed
        try {
            // Verificar si se ha seleccionado una fila
            int selectedRow = ProductosTable.getSelectedRow(); // Asegúrate de que ProdTable es el nombre correcto de tu tabla de productos
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Por favor, seleccione un producto en la tabla.");
                return;
            }

            // Obtener el ID del producto seleccionado
            int id = (int) ProductosTable.getValueAt(selectedRow, 0); // Suponiendo que el ID está en la primera columna

            // Mensaje de confirmación
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea borrar el producto?", "Confirmar Borrado", JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return; // Si el usuario selecciona "No", salir del método
            }

            String url = "jdbc:postgresql://localhost:5432/Papeleria";
            String user = "postgres";
            String password = "9656";
            Connection con = DriverManager.getConnection(url, user, password);

            // Cambiar el esquema activo
            Statement stmt = con.createStatement();
            stmt.execute("SET search_path TO ciber_action");

            // Consulta SQL para borrar un producto por su ID
            String sql = "DELETE FROM Producto WHERE Id_prod = ?";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.setInt(1, id);

            // Ejecutar la consulta
            int rowsDeleted = pstmt.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Producto eliminado exitosamente.");
                generarTable(); // Actualiza la tabla después de borrar
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró un producto con ese ID.");
            }

            pstmt.close();
            stmt.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar el producto: " + e.getMessage());
        }
    }//GEN-LAST:event_borrarProdActionPerformed

