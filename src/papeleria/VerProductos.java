    private void actualizarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarProdActionPerformed
        // Verificar si se ha seleccionado una fila
        int selectedRow = ProductosTable.getSelectedRow(); // Asegúrate de que ProdTable es el nombre correcto de tu tabla de productos
        if (selectedRow != -1) {
            // Convertir los valores obtenidos de la tabla a String de forma segura
            int id = (int) ProductosTable.getValueAt(selectedRow, 0);
            String nombre = ProductosTable.getValueAt(selectedRow, 1).toString();
            String descripcion = ProductosTable.getValueAt(selectedRow, 2).toString();
            String categoria = ProductosTable.getValueAt(selectedRow, 3).toString();
            String precio = ProductosTable.getValueAt(selectedRow, 4).toString(); // Asegúrate de que el precio se maneje correctamente

            // Abre el frame para actualizar producto
            new ActualizarProducto(id, nombre, descripcion, categoria, precio).setVisible(true);
            this.dispose(); // Cierra la ventana actual si es necesario
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un producto de la tabla.");
        }
    }//GEN-LAST:event_actualizarProdActionPerformed

